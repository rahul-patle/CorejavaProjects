package Array;

public class Student1 {

	int rollno, marks;

	public Student1(int rollno, int marks) {
		super();
		this.rollno = rollno;
		this.marks = marks;
	}

	public Student1() {
		super();

	}

	@Override
	public String toString() {
		return "Student [rollno=" + rollno + ", marks=" + marks + "]";
	}

}
