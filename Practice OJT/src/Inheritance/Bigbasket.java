package Inheritance;

public class Bigbasket {
	int bottlepack;
	int smallbasket;

	public Bigbasket(int bottlepack, int smallbasket) {
		super();
		this.bottlepack = bottlepack;
		this.smallbasket = smallbasket;
	}
public String toString () {

	return "["+" bottlepack " +bottlepack + " smallbasket "+smallbasket+"]";
	
}
}


