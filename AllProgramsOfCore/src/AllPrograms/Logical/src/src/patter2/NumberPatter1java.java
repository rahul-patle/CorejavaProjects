package patter2;

//11 12
//21 22

public class NumberPatter1java {
	
	public static void main(String[] args) {
		
		for(int i=1;i<=2;i++)
		{
			for(int j=1;j<=2;j++)
			{
				System.out.print(i+"" + j + " ");
			}
					
			System.out.println();
		}
	
	}
				//  i     j
				//  1     1
				//        2
				//  2	  1
				//        2
	
}
